    public static class Star
    {
        Star( int bscIndex, double elon, double elat, double vLon, double vLat, int dist, double mag, String name )
        {
            this.bscIndex = bscIndex;
            this.mag = (float)mag;
            position.set( dist * ly.toMeters(), elon * DEG, elat * DEG );
            velocity.set( 0, vLon, vLat );
        }

        private final int bscIndex;
        private final Ecliptical.Sphe position = new Ecliptical.Sphe();
        private final Ecliptical.Sphe velocity = new Ecliptical.Sphe();  // lon' and lat' in mas/a
        private final float mag;  //* Magnitude of star
    }

    public static final List<Star> STARS = new LinkedList<>();

    public static final Map<Integer, Ecliptical.Sphe> POSITIONS = new LinkedHashMap<>();
    public static final Map<Integer, Ecliptical.Sphe> VELOCITIES = new LinkedHashMap<>();
    public static final Map<Integer, Float> MAGNITUDES = new LinkedHashMap<>();
    public static final Map<Integer, String> NAMES = new LinkedHashMap<>();
    public static final List<Integer> BY_MAGNITUDE;

    static
    {
        initializeStarsListPart1();
        initializeStarsListPart2();
        initializeNames();
        for ( Star star : STARS )
        {
            POSITIONS.put( star.bscIndex, star.position );
            VELOCITIES.put( star.bscIndex, star.velocity );
            MAGNITUDES.put( star.bscIndex, star.mag );
        }
        BY_MAGNITUDE = new LinkedList<>( POSITIONS.keySet() );
        Collections.sort( BY_MAGNITUDE, new Comparator<Integer>()
        {
            @Override
            public int compare( Integer lhs, Integer rhs )
            {
                return Float.compare( MAGNITUDES.get( lhs ), MAGNITUDES.get( rhs ) );
            }
        } );
    }

    public static int findIndexByName( String name )
    {
        for ( int index : NAMES.keySet() )
            if ( NAMES.get( index ).equals( name ) )
                return index;
        return -1;
    }

